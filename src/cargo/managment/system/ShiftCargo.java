    public void validateField() {
        String orderId = (String) txtOrderId.getSelectedItem();
        String shiftedto = (String) txtShiftedTo.getSelectedItem();
        if (!orderId.equalsIgnoreCase("select") && !orderId.equals("") && !shiftedto.equalsIgnoreCase("select") && !shiftedto.equals("")) {
            btnShift.setEnabled(true);
        } else {
            btnShift.setEnabled(false);
        }
    }

    int capacity = 0;
    int quantity = 0;
    int result = 0;

    public void Result() {
        String id = (String) txtOrderId.getSelectedItem();
        String warehous = (String) txtShiftedTo.getSelectedItem();
        capacity = Integer.parseInt(txtCapacity.getText());
        quantity = Integer.parseInt(txtQuantity.getText());
        result = capacity - quantity;
        if (quantity > capacity) {
            JOptionPane.showMessageDialog(null, "Your cargo id '" + id + "' is overloaded for '" + warehous + "' ware house \n plz try another warehouse");

        } else {
            txtQuantityInwarehous.setText(String.valueOf(result));

        }
    }

    public void clear() {
        setVisible(false);
        new ShiftCargo().setVisible(true);
    }

