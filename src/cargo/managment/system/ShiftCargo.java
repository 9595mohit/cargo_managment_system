    private void btnShiftActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnShiftActionPerformed
        // TODO add your handling code here:
        ShiftCargoModel model = new ShiftCargoModel();
        model.setOrderId((String) txtOrderId.getSelectedItem());
        model.setPlace(txtPlace.getText());
        model.setQanity(txtQuantity.getText());
        model.setShiftedto((String) txtShiftedTo.getSelectedItem());
        model.setCapacity(txtCapacity.getText());
        model.setQuantityinwarehouse(txtQuantityInwarehous.getText());

        DateTimeFormatter dtf = DateTimeFormatter.ofPattern("dd/MM/yyyy");
        LocalDateTime now = LocalDateTime.now();
        String date = dtf.format(now);
        DateTimeFormatter dtf1 = DateTimeFormatter.ofPattern("HH:mm:ss");
        LocalDateTime now1 = LocalDateTime.now();
        String time = dtf1.format(now1);
        ShiftCargodao.saveData(model, date, time);
        clear();


    }//GEN-LAST:event_btnShiftActionPerformed

