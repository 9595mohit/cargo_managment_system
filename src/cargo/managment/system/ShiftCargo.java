    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        ArrayList<OrderCargo> lst = ShiftCargodao.getAllRecordsOrderCargo();
        Iterator<OrderCargo> itr = lst.iterator();
        while (itr.hasNext()) {
            OrderCargo ordercargo = itr.next();
            txtOrderId.addItem(ordercargo.getOrderid());

        }

        ArrayList<Warehouse> wareidlst = ShiftCargodao.getAllRecordsWarehous();
        Iterator<Warehouse> wareitr = wareidlst.iterator();
        while (wareitr.hasNext()) {
            Warehouse wr = wareitr.next();
            txtShiftedTo.addItem(wr.getPlace());
        }


    }//GEN-LAST:event_formComponentShown

    private void txtOrderIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtOrderIdActionPerformed
        // TODO add your handling code here:
        String orderid = (String) txtOrderId.getSelectedItem();
        OrderCargo ord = ShiftCargodao.getOrderCargoField(orderid);
        txtPlace.setText(ord.getWarehouseplace());
        txtQuantity.setText(ord.getQuantity());
        validateField();
        if(!txtCapacity.getText().equals(""))
        {
            Result();
        }

    }//GEN-LAST:event_txtOrderIdActionPerformed

