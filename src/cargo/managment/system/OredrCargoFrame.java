    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        OrderCargo ord=DataforOrdercargo.getId();
        int a=Integer.parseInt(ord.getOrderid())+1;
        lblOrderId.setText(String.valueOf(a));
        ArrayList<Warehouse> lst=WarehouseDao.getAllRecord();
        Iterator<Warehouse> itr=lst.iterator();
        while(itr.hasNext())
        {
            Warehouse warehousobj=itr.next();
            txtWarehousPlace.addItem(warehousobj.getPlace());  
        }
        
        
       
    }//GEN-LAST:event_formComponentShown

