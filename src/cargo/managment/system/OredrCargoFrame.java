    public void validateFiled()
    {
        String Quantity=txtQuantity.getText();
        String warehoseplace=(String) txtWarehousPlace.getSelectedItem();
        if(!Quantity.equals("")&&Quantity.matches(Quantityindigit)&& !warehoseplace.equals(""))
        {
            btnOrder.setEnabled(true);
        }
        else
        {
            btnOrder.setEnabled(false);
        }
        
    }
