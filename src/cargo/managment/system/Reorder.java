    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        Damagegoods dt=new Damagegoods();
        int index=jTable1.getSelectedRow();
        TableModel model= jTable1.getModel();
        String orderid=model.getValueAt(index,0).toString();
        String shiftedto=model.getValueAt(index, 1).toString();
        String damagequantity=model.getValueAt(index, 2).toString();
        dt.setOrderid(orderid);
        dt.setShiftedto(shiftedto);
        dt.setDamageQuantity(damagequantity);
        Damagegoodsdao.deleteData(orderid,shiftedto);
        setVisible(false);
        new Reorder().setVisible(true);
    }//GEN-LAST:event_jTable1MouseClicked

