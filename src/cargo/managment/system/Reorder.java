    public void getAllRecord() {
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        dtm.setRowCount(0);
        ArrayList<Damagegoods> lst=Damagegoodsdao.getData();
        Iterator<Damagegoods> itr=lst.iterator();
        while(itr.hasNext())
        {
            Damagegoods dt=itr.next();
            dtm.addRow(new Object[]{dt.getOrderid(),dt.getShiftedto(),dt.getDamageQuantity()});
        }
    }

